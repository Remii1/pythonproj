
x = 20

while x<30:
    print(x)
    x+=1

#Break statement
a = 5

while a<10:
    print(a)
    if a==8:
        break
    a+=1

#continue statement
b = 20

while b<30:
    b +=1
    if b==25:
        continue
    print(b)

#Continue statement
c = 80

while c<85:
    c +=1
    if c==83:
        continue
    print(c)